string s = "380995589012"; // Створюємо рядок з чисел, який будемо форматувати

int i = s.Length % 2 == 0 ? 2 : 1; // Визначаємо початковий індекс для вставки «-». Якщо довжина рядка парна, починаємо з 2, якщо непарна — з 1.

while (i < s.Length) // Запускаємо цикл, поки індекс i менший за довжину рядка
{
    if (i != s.Length - 1) // Перевіряємо, чи індекс i не дорівнює останньому символу рядка
    {
        s = s.Insert(i, "-"); // Вставляємо символ «-» на позиції i
        i += 3; // Збільшуємо індекс i на 3, щоб пропустити два символи після вставки «-»
    }
    else
    {
        i++; // Якщо це останній символ, просто збільшуємо індекс i на 1, щоб вийти з циклу
    }
}

Console.WriteLine(s); // Виводимо відформатований рядок на консоль
Console.ReadKey(); // Чекаємо, поки користувач натисне будь-яку клавішу перед завершенням програми
